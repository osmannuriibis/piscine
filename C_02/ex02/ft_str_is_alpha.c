#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	if (*str == '\0')
		return (1);
	while (*str != '\0')
	{
		if (*str < 'A' || *str > 'Z' && *str < 'a' || *str > 'z')
			return (0);
		str++;
	}
	return (1);
}
